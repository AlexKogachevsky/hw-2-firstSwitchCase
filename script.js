let number = +prompt("Enter an integer between zero and nine");

switch (number) {
  case 0:
    alert("Null");
    break;
  case 1:
    alert("One");
    break;
  case 2:
    alert("Two");
    break;
  case 3:
    alert("Three");
    break;
  case 4:
    alert("Four");
    break;
  case 5:
    alert("Five");
    break;
  case 6:
    alert("Six");
    break;
  case 7:
    alert("Seven");
    break;
  case 8:
    alert("Eight");
    break;
  case 9:
    alert("Nine");
    break;
  default:
    alert("Your number does not match the required parameters");
    break;
}
